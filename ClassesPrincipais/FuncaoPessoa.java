package ClassesPrincipais;

public class FuncaoPessoa {
    private String nomeFP,CPFFP, enderecoFP,nomeFuncaoFP;
    private double salarioFuncaoFP;

    //Getter e Setters
    public String getCPFFP() {
        return CPFFP;
    }
    public void setCPFFP(String CPFFP) {
        this.CPFFP = CPFFP;
    }
    public String getNomeFP() {
        return nomeFP;
    }
    public void setNomeFP(String nomeFP) {
        this.nomeFP = nomeFP;
    }
    public String getEnderecoFP() {
        return enderecoFP;
    }
    public void setEnderecoFP(String enderecoFP) {
        this.enderecoFP = enderecoFP;
    }
    public String getNomeFuncaoFP() {
        return nomeFuncaoFP;
    }
    public void setNomeFuncaoFP(String nomeFuncaoFP) {
        this.nomeFuncaoFP = nomeFuncaoFP;
    }
    public double getSalarioFuncaoFP() {
        return salarioFuncaoFP;
    }
    public void setSalarioFuncaoFP(double salarioFuncaoFP) {
        this.salarioFuncaoFP = salarioFuncaoFP;
    }


}
